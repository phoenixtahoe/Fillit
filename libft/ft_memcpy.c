#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char			*s;
	char			*d;
	unsigned int	i;

	i = 0;
	s = (char *)src;
	d = (char *)dst;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (d);
}
